using UnityEngine;
using System.Collections;
//========================================================================
// ■ Water_Spray
//------------------------------------------------------------------------
//	水飛沫のクラス。
//========================================================================

public class Water_Spray : MonoBehaviour {
	//--------------------------------------------------------------------
	// ● メンバ変数
	//--------------------------------------------------------------------
	ParticleEmitter[] emitters;
	//--------------------------------------------------------------------
	// ● 初期化
	//--------------------------------------------------------------------
	void Start() {
		emitters = GetComponentsInChildren<ParticleEmitter>();
		Destroy(gameObject, 5);
	}
	//--------------------------------------------------------------------
	// ● 更新
	//--------------------------------------------------------------------
	void Update() {
		foreach (var e in emitters)
			e.maxEmission *= 0.95f;
	}
}