using UnityEngine;
using System.Collections;
//========================================================================
// ■ Audio
//------------------------------------------------------------------------
//	音クラス。
//========================================================================

public class Audio {
	//--------------------------------------------------------------------
	// ● メンバ変数
	//--------------------------------------------------------------------
	public string name;			// 識別名
	public AudioClip clip;		// Unity 仕様の音データ
	//--------------------------------------------------------------------
	// ● コンストラクタ（基本）
	//--------------------------------------------------------------------
	public Audio() {
	}
	//--------------------------------------------------------------------
	// ● コンストラクタ（読み込み）
	//--------------------------------------------------------------------
	public Audio(ref string name, ref string file_name) {
		load(ref name, ref file_name);	// データ読み込み
	}
	//--------------------------------------------------------------------
	// ● 読み込み
	//--------------------------------------------------------------------
	public void load(ref string name, ref string file_name) {
		this.name = name;
		clip = Resources.Load<AudioClip>(file_name);	// リソースを読み込み
	}
	//--------------------------------------------------------------------
	// ● 解放
	//--------------------------------------------------------------------
	public void release() {
		name = null;
		Resources.UnloadAsset(clip);	// リソースを解放
	}
}