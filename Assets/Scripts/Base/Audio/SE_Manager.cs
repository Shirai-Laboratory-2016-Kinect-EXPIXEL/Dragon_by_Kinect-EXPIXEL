public class SE_Manager : Base_Audio_Manager {
	//--------------------------------------------------------------------
	// ● コンストラクタ
	//--------------------------------------------------------------------
	public SE_Manager() {
		path += "SE/";		// フォルダ階層を設定
	}
	//--------------------------------------------------------------------
	// ● 再生
	//--------------------------------------------------------------------
	override public void play(string name, string sub_path = "") {
		base.play(name, sub_path);				// 基盤クラスを再生
		source.PlayOneShot(audios[name].clip);	// 間引き再生
	}
}