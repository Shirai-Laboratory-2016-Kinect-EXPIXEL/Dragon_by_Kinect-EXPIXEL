using UnityEngine;
using System.Collections;
//========================================================================
// ■ Collision_Data
//------------------------------------------------------------------------
//	衝突判定の情報クラス。
//========================================================================

public class Collision_Data {
	//--------------------------------------------------------------------
	// ● 定数
	//--------------------------------------------------------------------
	public enum Type {
		ENTER,
		STAY,
		EXIT
	}
	//--------------------------------------------------------------------
	// ● メンバ変数
	//--------------------------------------------------------------------
	public Transform self;
	public Transform target;
	public Vector3 point;
	public Type type;
	public bool is_trigger;
	//--------------------------------------------------------------------
	// ● コンストラクタ
	//--------------------------------------------------------------------
	public Collision_Data(Transform self, Transform target, Vector3 point,
							Type type, bool is_trigger) {
		this.self = self;
		this.target = target;
		this.point = point;
		this.type = type;
		this.is_trigger = is_trigger;
	}
	//--------------------------------------------------------------------
	// ● 自身と違うか？
	//--------------------------------------------------------------------
	public bool is_not_self() {
		return self.root != target.root;
	}
}