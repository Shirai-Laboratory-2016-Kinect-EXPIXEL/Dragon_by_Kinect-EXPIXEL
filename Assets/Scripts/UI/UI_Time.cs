using UnityEngine;
using UnityEngine.UI;
using System.Collections;
using System;
//========================================================================
// ■ UI_Time
//------------------------------------------------------------------------
//	ＵＩの時間クラス。
//========================================================================

public class UI_Time : Mono_Behaviour_EX {
	//--------------------------------------------------------------------
	// ● メンバ変数
	//--------------------------------------------------------------------
	Text text;
	//--------------------------------------------------------------------
	// ● 初期化
	//--------------------------------------------------------------------
	void Start() {
		text = GetComponent<Text>();
	}
	//--------------------------------------------------------------------
	// ● 更新
	//--------------------------------------------------------------------
	void Update() {
		text.text = "" +
			DateTime.Now.Hour + ":" +
			DateTime.Now.Minute.ToString().PadLeft(2, '0');
	}
}